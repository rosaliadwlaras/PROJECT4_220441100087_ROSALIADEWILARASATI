    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        String matkul = tfMatkul.getText();
        java.util.Date deadlineDate = jDate.getDate();
        String detail = taDetail.getText();

        if (matkul.isEmpty() || deadlineDate == null || detail.isEmpty()) {
            JOptionPane.showMessageDialog(null, 
                    "Lengkapi data terlebih dahulu!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String deadline = dateFormat.format(deadlineDate);

            // Simpan data jika semua input telah diisi
            String namaDanTugas = nomor + ". " + matkul + " (dl: " + deadline + ")";
            listModel.addElement(namaDanTugas);
            detailModel.addElement(detail);
            nomor++;
        }
    }//GEN-LAST:event_btSimpanActionPerformed

