    private void lListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lListValueChanged
        // TODO add your handling code here:
        if (!evt.getValueIsAdjusting()) {
            int selectedIndex = lList.getSelectedIndex();
            if (selectedIndex != -1) {
                String detail = detailModel.getElementAt(selectedIndex); // Ambil detail sesuai dengan indeks
                taDetail2.setText(detail);
            }
        }
    }//GEN-LAST:event_lListValueChanged

    private void btnBersihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBersihActionPerformed
        // TODO add your handling code here:
        int[] selectedIndices = lList.getSelectedIndices();
    
        if (selectedIndices.length > 0) {
            for (int i = selectedIndices.length - 1; i >= 0; i--) {
                listModel.removeElementAt(selectedIndices[i]);
                detailModel.removeElementAt(selectedIndices[i]);
            }

            for (int i = 0; i < listModel.getSize(); i++) {
                String existingItem = listModel.getElementAt(i);
                String[] parts = existingItem.split("\\. ");
                int existingNomor = i + 1;
                String newItem = existingNomor + ". " + parts[1];
                listModel.setElementAt(newItem, i);
            }
            taDetail2.setText("");
            nomor = 1;
        } else {
            listModel.removeAllElements();
            detailModel.removeAllElements();
            taDetail2.setText("");
            nomor = 1;
        }
    }//GEN-LAST:event_btnBersihActionPerformed


